// Helper to recolor characters
function recolor(lines, from, to) {
  return lines.map(row => row.replace(new RegExp(from, 'g'), to));
}

function makeChar(shirtColor, hairColor = '3') {
  let idle = recolor(charIdle, '2', shirtColor);
  idle = recolor(idle, '3', hairColor);
  let w1 = recolor(charWalk1, '2', shirtColor);
  w1 = recolor(w1, '3', hairColor);
  let w2 = recolor(charWalk2, '2', shirtColor);
  w2 = recolor(w2, '3', hairColor);
  return { idle: buildSprite(idle), walk1: buildSprite(w1), walk2: buildSprite(w2) };
}

